    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       
        String fname1 = jfname.getText();
        String lname1 = jlname.getText();
        String mname1 = jmname.getText();
        String death1 = "Alive";
        if(radioyes.isSelected())
        {
            death1 = "Deceased";
        }
        String gender1 = "Male";
        if(jRadioButtonFemale.isSelected())
        {
            gender1 = "Female";
        }
        String age1 = jage.getText();
        
        String queryfind = "UPDATE ancestor_tb SET firstname = ?, lastname = ?, middlename= ?, gender = ?, death = ?, age = ?, birthdate = ? WHERE id=?";
        // "INSERT INTO `ancestor_tb`(`firstname`, `lastname`, `middlename`, `gender`, `death`, `age`, `birthdate`, `imgfile`) VALUES (?,?,?,?,?,?,?,?)";
           
          
            try{
            
            ps = connect.getConnection().prepareStatement(queryfind);
            ps.setString(1, fname1);
            ps.setString(2, lname1);
            ps.setString(3, mname1);
            ps.setString(4, gender1);
            ps.setString(5, death1);
            ps.setString(6, age1);
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String date = sdf.format(jDateChooser1.getDate());
            ps.setString(7, date); 
            ps.setInt(8, tempoid);
            ps.executeUpdate();
            if(ps.executeUpdate() != 0){
                JOptionPane.showMessageDialog(null, "Ancestor Has Been Updated");
                
            }else{
                JOptionPane.showMessageDialog(null, "Error");
            } 
            
            
            }catch (SQLException ex) {
            Logger.getLogger(LoginForm.class.getName()).log(Level.SEVERE, null, ex);
            } 
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

