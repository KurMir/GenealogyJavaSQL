    public EditAncestor(int forid){
    
    PreparedStatement st;
        ResultSet rs;
        String query = "SELECT * FROM `ancestor_tb` WHERE `id` = ?";
       
        try {
            st = geneadmin.connect.getConnection().prepareStatement(query);    
            st.setInt(1, forid);
            rs = st.executeQuery();              
            if(rs.next()){
                tempoid = forid;
                fname = rs.getString("firstname");
                lname = rs.getString("lastname");
                mname = rs.getString("middlename");
                gender = rs.getString("gender");
                dth = rs.getString("death");
                age = rs.getString("age");
                date = rs.getString("birthdate");
                
                
                   
            }else{
                //error message
                JOptionPane.showMessageDialog(null, "Login Error","Login Error", 2);         
            }    
        } catch (SQLException ex) {
            Logger.getLogger(geneadmin.LoginForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    }
    
